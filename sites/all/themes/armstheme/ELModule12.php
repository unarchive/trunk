<h1>Policy and procedures</h1>
<br>
The policy in a records management programme sets the overall targets and goals.  It takes as its framework the lifecycle of records and establishes roles and responsibilities which we’ll come back below.  Procedures then provide practical guidelines on how to implement the policy objectives.<br>
<br>
Key areas for policy and procedure development feature in the following elearning modules:<br>
<ul>
<li><b>Records creation and capture into filing/record keeping systems<br>
Creation</b> is the first stage in the life of any record.  A record by definition is the output of a business transaction.  It provides evidence of a completed business transaction and contains information about the business transaction.  All UN staff creates records as part of their work. Nowadays most records are created electronically and live out the whole of their life electronically.
<b>Capture</b> is the process of incorporating a record within an organizational records system and is a vital part of the records lifecycle<br><br>
<li><b>Records classification schemes/taxonomies for recordkeeping<br>
Records classification schemes</b> categorise records in a systematic and consistent manner which facilitates their capture, retrieval, maintenance and disposal.  They also establish final naming conventions, security restrictions and access rights to records. <b>Taxonomies for recordkeeping</b> have two main purposes as follows:
<ul>
<li>to identify and retrieve records within and across business units
<li>to organise records to facilitate the implementation of corporate records management policies
</ul>
<br>
<li><b>Records access and copies</b><br>
Records access can apply both to records which are no longer in the creating office or to those records which have become part of the archives.  But not everyone has the same rights of access and not everything is accessible.<br><br>
<li><b>Record copies</b><br>
One of the ironies of the increased use of information technology and the promise of the paperless office is that paper and electronic copies of records proliferate exponentially. It is essential to know which the official/record copies are and which are the non-record/transitory copies and manage each of them appropriately<br><br>
<li><b>Managing email or other special media records</b><br>
Email has become an important business and personal communication tool within the UN.  It is estimated that between 85% and 90% of email messages are either transitory records or personal communications but that two thirds of business decisions are now recorded within email.  The most important thing to remember about email, electronic documents or information on alternate media is that based on the subject content and other characteristics it must be treated with the same degree of seriousness as paper and other traditional media records.<br><br>
<li><b>Retention Schedules, Records transfer and disposal</b><br>
Not all information and records have the same value and only a small proportion (less than 10%) make up the future archives on the UN. Basic principles and objectives of good recordkeeping are supported by <b>retention schedules</b> which provide practical guidance that can be applied to major groups of records used by many offices. You should use available retention schedules to guide decisions on what to dispose, what to keep and for how long. Records created by staff members need to be stored appropriately for as long as they are needed and no longer.  The purpose of <b>records transfer</b> is to ensure that the records are safely and securely stored for the period that they need to be retained. Very few records created by any part of the organisation will have the kind of value which warrants their permanent retention as historical archives.  All other records (possibly as many as 95%) need to be <b>destroyed securely in a controlled manner</b>. This is not only good practice but it is essential to complete the recordkeeping cycle.<br><br>
<li><b>Risk management and Disaster recovery</b><br>
Because records contain information about and provide evidence of business transactions they play an important role in protecting every organization on a day to day business and a crucial role in the event of a disaster.  When a disaster occurs having the appropriate records to hand (these are called vital records) can make the difference between whether the organisation can continue to operate in the immediate aftermath and/or survive at all in the longer term.  Hazards and threats to records come from a variety of sources.  The risk management module provides more details on risk analysis, Identifying hazards, assessment of likelihood and impact and risk management.  It is impossible to prevent all disasters.  Weather patterns change, accidents happen and malicious actions, whether by an individual or on a larger scale, are difficult to predict.  It is therefore crucial to be prepared so that, if a disaster does occur, its impact can be minimized and normal service can be resumed as soon as possible.<br><br>
<li><b>Archives management</b> <br>
Archival records are those records, irrespective of format, which are to be permanently preserved for their administrative, legal, historical or informational value as evidence of the official business of the organisation.  Typically they represent less than 10% of the total records of an organisation. They need special treatment both in terms of their logical or intellectual organisation as well as their physical management. Arrangements have to be made so that eligible individuals and organisations can get access to those archival records which can be consulted under conditions where the archival records are handled with due care.<br><br>
</ul>
