<br>
Once you have calculated the likelihood you then need to work out the impact of the hazard on the office operations.  Again the impact can be calculated on a scale as follows:<br>
<ul>
<li>High (will put you out of business) = 10 points
<li>Medium (interrupt business operations ) = 5 points
<li>Low (minor disruption) = 1.
</ul>
<br>
